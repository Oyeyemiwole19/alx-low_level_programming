#include <stdio.h>
#include <stdlib.h>
/**
 * op_add- addition
 * @a: first number
 * @b: second number
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub- substract
 * @a: first numner
 * @b: second number
 * Return: the difference of and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul- multiplication
 * @a: first number
 * @b: second number
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div- division
 * @a: first number
 * @b: second number
 * Return: the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod- modulus
 * @a: first number
 * @b: seond number
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
